# Atribuição de valores a variáveis
peso = 55
print(peso)  # Resultado: 55

# Atribuição com operação
peso1 = 100
peso1 = peso1 + 50  # Atribuindo o novo valor à variável peso1
#peso1 += 50  # Alternativa para o código acima, usando o operador de atribuição composto
print(peso1)  # Resultado: 150

# Outros operadores de atribuição
peso2 = 10
peso2 *= 2  # Multiplica e atribui
print(peso2)  # Resultado: 20

peso3 = 20
peso3 /= 4  # Divide e atribui
print(peso3)  # Resultado: 5.0

peso4 = 8
peso4 **= 2  # Eleva à potência e atribui
print(peso4)  # Resultado: 64

peso5 = 12
peso5 %= 5  # Atribui o resto da divisão
print(peso5)  # Resultado: 2
