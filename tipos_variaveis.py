# Exemplos de tipos de variáveis em Python

# 1. Inteiros (int)
a = 10
print(f"Tipo de 'a': {type(a)}")  # Tipo: <class 'int'>
print(f"Valor de 'a': {a}")  # Valor: 10

# 2. Flutuantes (float)
b = 3.14
print(f"Tipo de 'b': {type(b)}")  # Tipo: <class 'float'>
print(f"Valor de 'b': {b}")  # Valor: 3.14

# 3. Strings (str)
nome = "Maria"
print(f"Tipo de 'nome': {type(nome)}")  # Tipo: <class 'str'>
print(f"Valor de 'nome': {nome}")  # Valor: Maria

# 4. Booleanos (bool)
c = True
d = False
print(f"Tipo de 'c': {type(c)}")  # Tipo: <class 'bool'>
print(f"Valor de 'c': {c}")  # Valor: True
print(f"Tipo de 'd': {type(d)}")  # Tipo: <class 'bool'>
print(f"Valor de 'd': {d}")  # Valor: False

# 5. Listas (list)
lista = [1, 2, 3, 4, 5]
print(f"Tipo de 'lista': {type(lista)}")  # Tipo: <class 'list'>
print(f"Valor de 'lista': {lista}")  # Valor: [1, 2, 3, 4, 5]

# 6. Tuplas (tuple)
tupla = (1, 2, 3)
print(f"Tipo de 'tupla': {type(tupla)}")  # Tipo: <class 'tuple'>
print(f"Valor de 'tupla': {tupla}")  # Valor: (1, 2, 3)

# 7. Dicionários (dict)
dicionario = {"nome": "João", "idade": 30}
print(f"Tipo de 'dicionario': {type(dicionario)}")  # Tipo: <class 'dict'>
print(f"Valor de 'dicionario': {dicionario}")  # Valor: {'nome': 'João', 'idade': 30}

# 8. Conjuntos (set)
conjunto = {1, 2, 3, 4, 5}
print(f"Tipo de 'conjunto': {type(conjunto)}")  # Tipo: <class 'set'>
print(f"Valor de 'conjunto': {conjunto}")  # Valor: {1, 2, 3, 4, 5}
