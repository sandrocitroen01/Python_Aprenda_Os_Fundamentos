# Definindo as variáveis
a = 3
b = 5

# Comparações usando operadores de comparação

print(a == b)  # Igual: Verifica se os valores de a e b são iguais. Resultado: False
print(a != b)  # Diferente: Verifica se os valores de a e b são diferentes. Resultado: True
print(a > b)   # Maior que: Verifica se a é maior que b. Resultado: False
print(a < b)   # Menor que: Verifica se a é menor que b. Resultado: True
print(a >= b)  # Maior ou igual: Verifica se a é maior ou igual a b. Resultado: False
print(a <= b)  # Menor ou igual: Verifica se a é menor ou igual a b. Resultado: True
